            <p>たび重なるトラブルを乗り越えて、見事小惑星イトカワの微粒子を持ち帰り、大きな話題となった『はやぶさプロジェクト』。</p>
            <p>その成功の裏には多くの人と企業の支えがありました。</p>

            <p>相模原市で金属加工を行う株式会社コバヤシ精密工業も、このプロジェクトを支えた中小企業の一つ。</p>
            <p>同社は独立行政法人宇宙航空研究開発機構様（以下、JAXA）と共に、小惑星イトカワで収集した物質が入ったカプセルの分解作業を手伝い、「月以外の天体のサンプル収集」成功の確認と調査に協力しました。</p>

            <p class="lead" ><b>カプセルの取り出しに成功。微粒子サンプルを確認</b></p>
            分解してとりだされたカプセルを見た瞬間は、まず「やったぞ！」という思いでした。 後は、無事に中身が発見されてくれればよいな、と思いました。作業が終わってからサンプルが確認できるまでは本当にドキドキしていました。サンプルが発見されたと聞いた時は嬉しかったですね。
